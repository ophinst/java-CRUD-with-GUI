    private void showData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Address");
        model.addColumn("Phone_Number");
        model.addColumn("E-Mail");
        model.addColumn("Album_Name");
        model.addColumn("Quantity");
        model.addColumn("Version");
        model.addColumn("Payment_Method");
        
        try{
            String sql = "SELECT * FROM order_details";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{res.getString(1), res.getString(2), 
                             res.getString(3), res.getString(4), res.getString(5), res.getString(6),res.getString(7),
                             res.getString(8),res.getString(9),});
            }
            table_order.setModel(model);
        }catch(SQLException e){
            System.out.println("Error " + e.getMessage());
        }
    }
