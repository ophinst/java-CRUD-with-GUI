    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "DELETE FROM order_details WHERE name = '" + name.getText() +"'";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Delete data successful");
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        showData();
        blank_form();
        
    }//GEN-LAST:event_deleteActionPerformed

    private void table_orderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_orderMouseClicked
        // TODO add your handling code here:
        int row = table_order.rowAtPoint(evt.getPoint());
        String ID = table_order.getValueAt(row, 0).toString();
        id.setText(ID);
        
        String Customers_Name = table_order.getValueAt(row, 1).toString();
        name.setText(Customers_Name);

        String Customers_Address = table_order.getValueAt(row, 2).toString();
        address.setText(Customers_Address);

        String Customers_Phone = table_order.getValueAt(row, 3).toString();
        phone.setText(Customers_Phone);

        String Mail = table_order.getValueAt(row, 4).toString();
        email.setText(Mail);

        String Album_Name = table_order.getValueAt(row, 5).toString();
        album.setText(Album_Name);
        
        String Qty = table_order.getValueAt(row, 6).toString();
        quantity.setText(Qty);
        
        String Album_Version = table_order.getValueAt(row, 7).toString();
        ver.setText(Album_Version);
        
        String Payment_Method = table_order.getValueAt(row, 8).toString();
        method.setText(Payment_Method);
    }//GEN-LAST:event_table_orderMouseClicked

